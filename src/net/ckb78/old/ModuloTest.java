package net.ckb78.old;

public class ModuloTest {
    public static void main(String[] args){

        // returns True if 15 divides evenly with 5
        System.out.println(15 % 5 == 0);

        // returns false if 15 does no divide evenly with 4
        System.out.println(15 % 4  == 0);

        /* checks if number is even or odd - returns true if number is even because only even numbers
         will give a remainder of 0 if divided by two */
        System.out.println(4 % 2 == 0);

    }
}
